    // sortiranje
    Comparator<Fakultet> usporediNaziv = new Comparator<Fakultet>() {
	Locale croatian = new Locale("hr", "HR");
	Collator collator = Collator.getInstance(croatian);
	@Override
	public int compare(Fakultet f1, Fakultet f2) {
		return collator.compare(f1.getNaziv(),f2.getNaziv());
	}
    };
    
