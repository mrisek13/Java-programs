    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // filtriranje podataka prema odabranoj županiji
        int id_zupanija = jComboBox2.getSelectedIndex();
        if(id_zupanija<=0)
            obnoviPopis();
        else
            filtrirajPodatak(id_zupanija);
    }//GEN-LAST:event_jButton4ActionPerformed

    void filtrirajPodatak(int id_zupanija){
       ((DefaultListModel) jList1.getModel()).removeAllElements();
        for(int x=0;x<listaFakulteta.size();x++){
            Fakultet data=(Fakultet)listaFakulteta.get(x);
            if(data.getZupanija()!=id_zupanija)
                continue;
            ((DefaultListModel) jList1.getModel()).addElement( data.getNaziv()+"-"+data.getGrad());
        } 
    }
    
  
    void obnoviPopis(){
        ((DefaultListModel) jList1.getModel()).removeAllElements();
        for(int x=0;x<listaFakulteta.size();x++){
            Fakultet data=(Fakultet)listaFakulteta.get(x);
             ((DefaultListModel) jList1.getModel()).addElement( data.getNaziv()+"-"+data.getGrad());
        }
    }
    
    

