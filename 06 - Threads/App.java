private void prognoza() {
    
    String unos = jTextArea1.getText();
    
    
    List<String> prognoze = new ArrayList<>();
    
    try {
        
        String[] redovi = unos.split("\n");
        for (String red : redovi) {
            prognoze.add(red);
        }

        
        prognoze.sort((p1, p2) -> {
            try {
                String[] dijelovi1 = p1.split(", ");
                String[] dijelovi2 = p2.split(", ");
                
                // Datum
                Date datum1 = new SimpleDateFormat("dd.MM.yyyy").parse(dijelovi1[1]);
                Date datum2 = new SimpleDateFormat("dd.MM.yyyy").parse(dijelovi2[1]);
                
                
                int usporedbaDatuma = datum1.compareTo(datum2);
                if (usporedbaDatuma != 0) {
                    return usporedbaDatuma;
                }
                
               
                int temperatura1 = Integer.parseInt(dijelovi1[2]);
                int temperatura2 = Integer.parseInt(dijelovi2[2]);
                return Integer.compare(temperatura1, temperatura2);
            } catch (Exception e) {
                e.printStackTrace();
                return 0;
            }
        });

        
        jTextArea1.setText(String.join("\n", prognoze));
    } catch (Exception e) {
        
        JOptionPane.showMessageDialog(this, "Gre≈°ka prilikom obrade podataka: " + e.getMessage());
    }
      
    
}

class IspisImena implements Runnable {
    private String ime;

    public IspisImena(String ime) {
        this.ime = ime;
    }

    @Override
    public void run() {
        try {
            for (int i = 0; i < 10; i++) {
               
                SwingUtilities.invokeLater(() -> {
                    jTextArea1.append("\n" + ime);
                });
                Thread.sleep(3000); 
            }
        } catch (InterruptedException e) {
            Thread.currentThread().interrupt();
            JOptionPane.showMessageDialog(null, "Prekid ispisa.");
        }
    }
}

